package com.example.ikesios;

public class OtherMember {

    public int Age ;
    public int Gender;
    public String City;
    public int PeopleLivingWith;

    public int getAge() {
        return Age;
    }

    public void setAge(int age) {
        Age = age;
    }

    public int getGender() {
        return Gender;
    }

    public void setGender(int gender) {
        Gender = gender;
    }

    public String getCity() {
        return City;
    }

    public void setCity(String city) {
        City = city;
    }

    public int getPeopleLivingWith() {
        return PeopleLivingWith;
    }

    public void setPeopleLivingWith(int peopleLivingWith) {
        PeopleLivingWith = peopleLivingWith;
    }
}
